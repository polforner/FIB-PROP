package domini.Clases;
/**
 * Classe que defineix la interficie de un resultat de una cosulta
 */
public interface ResultatConsulta{
    /**
     * Ordena alfabeticament el resultat
     */
    public void ordenarAlfabeticament();

    /**
     * Ordena per ordre alfabetic inversament el resultat
     */
    public void ordenarInversament();
}